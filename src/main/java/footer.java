public class footer {
        String footer;

    public footer(String footer) {
        this.footer = footer;
    }

    @java.lang.Override
    public java.lang.String toString() {
        return "footer{" +
                "footer='" + footer + '\'' +
                '}';
    }

    public String getFooter() {
        return footer;
    }

    public void setFooter(String footer) {
        this.footer = footer;
    }
}
